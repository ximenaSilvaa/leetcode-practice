// My solution
class Solution {
public:
    vector<int> twoSum(vector<int>& nums, int target) {
        unordered_map<int, int> numMap;
        int n = nums.size();

        // Llenamos el mapa con valor -> Ã­ndice
        for (int i = 0; i < n; i++) {
            numMap[nums[i]] = i;
        }

        // Buscamos si existe el complemento
        for (int i = 0; i < n; i++) {
            int complement = target - nums[i];
            if (numMap.count(complement) && numMap[complement] != i) {
                return {i, numMap[complement]};
            }
        }

        return {}; // Si no se encuentra ninguna pareja
    }
};

